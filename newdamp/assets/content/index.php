<div class="section">
    <h1>Introduction</h1>
    <div class="content">
        The Department Academic Mentorship Programme (D-AMP), under the aegis of the Institute Student Mentorship Programme (ISMP) identifies and selects senior student mentors from each department to help mentees/students cope with any problems they may face in the department or otherwise. The program helps sophomore students for a smooth transition into the department and deals with students in undergraduate departments who are facing severe academic problems.
        <br><br>
        The primary aim is to provide a support system within the department to help students with academic and personal problems. The program also aims to assist weak students to clear their backlogs and get their academic careers back on track. The Department Academic Mentors, along with the Faculty Advisors and Department Faculty Coordinators, work towards achieving this goal through early identification and constant monitoring of the students in question. D-AMP mentoring is a combination of both mentoring and monitoring to focus the energies of the students in the right direction.
    </div>
</div>

<div class="section">
    <h1>Objectives</h1>
    <div class="content">
        <ul>
            <li>To make sure that every student has a motivated and well-rounded senior student from his/ her own department, someone whom he/she can connect with, to be approached in case of need for support and guidance.</li>
            <li>To help the academically weak students improve their performance.</li>
            <li>To help the students structure their academic calendar for the smooth and timely completion of their studies.</li>
            <li>To act as a communication channel between the faculty and students, and respond proactively to the needs of both.</li>
            <li>To provide inputs and help carry out any new initiatives of the D-AMP that would be beneficial to the department such as course mentoring, collecting detailed information regarding courses and reference material, curriculum review and restructuring, increasing awareness about opportunities in the department among the student body, etc.</li>
        </ul>
    </div>
</div>

<div class="section">
    <h1>Structure of DAMP</h1>
    <div class="content">
        The D-AMP Mentors act under the supervision of the respective D-AMP Coordinators in consultation with the SMP Coordinators and Cabinet. DAMP Coordinators will discuss Plans-of-Action and new initiatives with the SMP coordinators and Cabinet.
        <br><br>

        <div class="card-container">
            <div class="card">
                <div class="card-title">
                    Department Faculty Coordinator
                </div>
                <div class="card-description">
                    One Faculty member from each department to be nominated by the Head of the Department and mutual consent of the faculty member, is to act as the faculty in-charge of the D-AMP in each department. The DAMP Coordinator must work closely with the Faculty Coordinator to implement their programmes effectively.
                </div>
            </div>
    
            <div class="card">
                <div class="card-title">
                    Faculty Advisors
                </div>
                <div class="card-description">
                    The Faculty Advisors of each undergraduate batch play a vital role in the running of the programme. Apart from assisting the students in matters pertaining to general academics, registration and course adjustment, a Faculty Advisor is the Faculty member who best knows the students. A D-AMP mentor may refer a ‘problem case’ to the Faculty Advisor of the concerned student, who may then step in and ensure that the student improves his/her performance.
                </div>
            </div>
    
            <div class="card">
                <div class="card-title">
                    Department Academic Mentor Coordinators
                </div>
                <div class="card-description">
                    One or two well-rounded students from each department with strong leadership qualities would be made responsible for the day to day functioning of the D-AMP of the department they belong to. They would report directly to the Department Faculty Coordinator, the SMP Coordinators and Cabinet.
                </div>
            </div>
    
            <div class="card">
                <div class="card-title">
                    Department Academic Mentors
                </div>
                <div class="card-description">
                    Select students from the 3rd/4th/5th year from each department will constitute the team of their respective D-AMP. Each of them would be assigned a group of students, usually junior to them, who would be the direct point of interaction with those students and should report special cases for the consideration of DAMP Coordinators/ SMP Cabinet Members/ SMP Coordinators.
                </div>
            </div>
        </div>
